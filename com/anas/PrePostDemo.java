package com.anas;

public class PrePostDemo {
	public static void main (String[] args) {
		int i = 3;
		i++;
		
		// prints 4
		System.out.println(i);
		++i;
		// printers 5
		System.out.println(i);
		// printers 6
		System.out.println(++i);
		//printers 6
		System.out.println(i++);
		//printers 7
		System.out.println(i);
	}	
	

		}
